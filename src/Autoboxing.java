
public class Autoboxing {

	public static void main(String[] args) {
	    Integer num1 = 127;
	    Integer num2 = 127;
	    Integer num3 = 128;
	    Integer num4 = 128;
	    
	    
	    if(num1==num2){
	      System.out.println("num1 == num2");
	    }
	    else{
	      System.out.println("num1 != num2");
	    }
	    if(num3==num4){
	      System.out.println("num3 == num4");
	    }
	    else{
	      System.out.println("num3 != num4");
	    }
}
}
